"""
Healthcare task definitions and profiles.

Each HealthcareTask represents one sensing/processing job generated by a
wearable device.  Task profiles are derived from published literature on
clinical-grade biosignal processing workloads.

References:
  - ECG workload: Pan-Tompkins + DNN classifier ≈ 12 M cycles, 5 MB data
  - SpO2: simple LED-ratio algorithm, ~500 K cycles, 5 KB data
  - BP (cuffless PPG): 2 M cycles, 50 KB data
  - Multi-vital fusion: aggregated pipeline, ~8 M cycles, 1 MB data
"""

from dataclasses import dataclass, field
from typing import Optional


@dataclass
class HealthcareTask:
    task_id: int
    device_id: int
    timestamp: float              # arrival time in seconds since simulation start
    data_size_bits: int           # D_i — raw data payload in bits
    cpu_cycles: int               # C_i — required CPU cycles for local processing
    max_delay_s: float            # T^{max}_i — SLA deadline in seconds
    privacy_sensitivity: float    # ρ_i ∈ [0, 1] — higher → more sensitive
    ci_score: float               # Φ_i ∈ [0, 1] — Criticality Index
    attack_probability: float = 0.0   # p_{atk,i} ∈ [0, 1] — adversarial threat level
    source: str = 'synthetic'         # 'synthetic' | 'physionet' | 'mimic'
    assigned_node: Optional[int] = None
    actual_latency_s: Optional[float] = None
    actual_energy_j: Optional[float] = None
    sla_violated: bool = False

    def __post_init__(self):
        # Clamp CI and privacy to [0, 1]
        self.ci_score = float(max(0.0, min(1.0, self.ci_score)))
        self.privacy_sensitivity = float(max(0.0, min(1.0, self.privacy_sensitivity)))
        self.attack_probability = float(max(0.0, min(1.0, self.attack_probability)))


